class IPGenerator:
    """Zmap-style IP generator — scans consecutive /24 subnets within provider blocks."""

    def __init__(self, shuffle_blocks: bool = True):
        """
        shuffle_blocks=True: randomize which provider block to scan next
        shuffle_blocks=False: sequential through provider blocks (for reproducibility)
        """
        # Build list of all /24 subnets: (base_int, count_of_256IPs_in_that_range)
        self.subnets = []
        for base_ip_str, count in PROVIDER_BLOCKS:
            parts = list(map(int, base_ip_str.split(".")))
            base_int = (parts[0] << 24) | (parts[1] << 16) | (parts[2] << 8)
            for offset in range(count):
                subnet_int = base_int + (offset << 8)
                self.subnets.append(subnet_int)

        # Shuffle subnets so we're not just blasting one provider first
        self.rng = random.Random()
        if shuffle_blocks:
            self.rng.shuffle(self.subnets)

        self.index = 0
        self.current_ip_offset = 0
        self.total = len(self.subnets)

    def next_ip(self) -> str:
        """Get next IP — scans a /24 sequentially, then moves to next subnet."""
        subnet = self.subnets[self.index]
        ip_int = subnet + self.current_ip_offset

        self.current_ip_offset += 1
        if self.current_ip_offset >= 256:
            self.current_ip_offset = 0
            self.index += 1
            if self.index >= self.total:
                self.index = 0  # wrap around

        # Skip 0 and 255 in each subnet (network/broadcast)
        if (ip_int & 0xFF) in (0, 255):
            return self.next_ip()

        return f"{(ip_int >> 24) & 0xFF}.{(ip_int >> 16) & 0xFF}.{(ip_int >> 8) & 0xFF}.{ip_int & 0xFF}"

    def batch_ips(self, count: int) -> list[str]:
        return [self.next_ip() for _ in range(count)]


def ip_generator():
    gen = IPGenerator()
    while True:
        yield gen.next_ip()
